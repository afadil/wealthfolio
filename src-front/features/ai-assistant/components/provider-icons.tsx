// Icon registry mapping icon names from ai_providers.json to React components
const icons = {
  LogoOllama,
  LogoGroq,
  LogoGoogle,
  LogoOpenAI,
  LogoAnthropic,
  LogoOpenRouter,
} as const;

type IconName = keyof typeof icons;

interface ProviderIconProps {
  name: string;
  size?: number;
  className?: string;
}

/**
 * Renders a provider icon by name.
 * Falls back to a generic Bot icon if the name is not found.
 *
 * @example
 * <ProviderIcon name="LogoOllama" size={16} />
 */
export function ProviderIcon({ name, size = 16, className }: ProviderIconProps) {
  const IconComponent = icons[name as IconName];

  if (IconComponent) {
    return <IconComponent size={size} className={className} />;
  }

  return <BotIcon size={size} className={className} />;
}
